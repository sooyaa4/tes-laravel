    <script>
        $('#savedata').click(function(e) {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            var isi = {
                'email': $('#floatingInput').val(),
                'password': $('#floatingPassword').val()
            }
            e.preventDefault();
            $(this).html('Sending..');
            $.ajax({
                url: "{{ url('api/post/auth/get') }}",
                method: "POST",
                dataType: 'json',
                data: isi,
                success: function(data) {
                    Swal.fire({
                        icon: "success",
                        title: "Berhasil Login!",
                        customClass: {
                            confirmButton: "btn btn-success"
                        },
                    });
                    $('#postForm').trigger("reset");
                    setTimeout(function() {
                        window.location.href = "{{ route('data.post') }}";
                    }, 1500);
                },
                error: function(data) {
                    Swal.fire({
                        icon: 'error',
                        title: data.responseJSON.message,
                        showConfirmButton: false,
                        timer: 3000
                    });
                    $('#savedata').html('Sign in');
                }
            });
        });
    </script>
